package com.BasicJava;

public class DoWhileExample 
{

	public static void main(String[] args) 
	{
	int a=0;
	do
	{
		System.out.println(a+" "+"Selenium");
		a++;
	}
	while (a<=2);
	System.out.println("********");
	
	int b=2;
	do
	{
		System.out.println(b+" "+"Selenium");
		b--;
	}
	while (b>=0);
	}

}
