package com.BasicJava;

public class WhileExample3 
{

	public static void main(String[] args) 
	{
		int i=0;
		while (i<=3)
		{
			System.out.println(i+" "+"Selenium");
			i++;
		}
		System.out.println("#########");
		
		int j=3;
		while (j>=0)
		{
			System.out.println(j+" "+"QTP");
			j--;
		}
			
		
	}

}
